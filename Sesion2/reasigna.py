#Programa para uso de operaadores de reasignacion

numero = input('Digite un número: ')
print('El nuemero digitado es: ', numero,'y es de tipo: ', type(numero))

numero+=10 #Número = número + 10
print("(+=) el nuevo número es: ",numero)
numero-=10 #Número = número - 10
print("(-=) el nuevo número es: ",numero)
numero*=10 #Número = número * 10
print("(*=) el nuevo número es: ",numero)
numero/=10 #Número = número / 10
print("(/=) el nuevo número es: ",numero)